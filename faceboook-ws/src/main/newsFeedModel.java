package main;

public class newsFeedModel {
	private String userName;
	private String groupName;
	private String activity_type;
	private String friendName;
	private String status;
	private String lifeEventType;
	private String lifeMessage;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getActivity_type() {
		return activity_type;
	}
	public void setActivity_type(String activity_type) {
		this.activity_type = activity_type;
	}
	public String getFriendName() {
		return friendName;
	}
	public void setFriendName(String friendName) {
		this.friendName = friendName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getLifeEventType() {
		return lifeEventType;
	}
	public void setLifeEventType(String lifeEventType) {
		this.lifeEventType = lifeEventType;
	}
	public String getLifeMessage() {
		return lifeMessage;
	}
	public void setLifeMessage(String lifeMessage) {
		this.lifeMessage = lifeMessage;
	}
	
}
